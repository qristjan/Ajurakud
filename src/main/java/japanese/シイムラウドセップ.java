package japanese;

public class シイムラウドセップ {

	private String なまえ;
	private int ニミ;
	private String 職業;

	public String getなまえ()
	{
		return なまえ;
	}

	public void setなまえ(String なまえ)
	{
		this.なまえ = なまえ;
	}

	public int getニミ()
	{
		return ニミ;
	}

	public void setニミ(int ニミ)
	{
		this.ニミ = ニミ;
	}

	public String get職業()
	{
		return 職業;
	}

	public void set職業(String 職業)
	{
		this.職業 = 職業;
	}

	public シイムラウドセップ(String なまえ, int ニミ, String 職業)
	{
		this.なまえ = なまえ;
		this.ニミ = ニミ;
		this.職業 = 職業;
	}

}
